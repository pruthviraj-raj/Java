public class method11 {
    /*
    check even or odd
     */
    static boolean oddeven(int n){
        return n%2==0;
    }
    public static void main(String[] args) {
        
        if(oddeven(10)){
            System.out.println("even");
        }
        else{
            System.out.println("odd");
        }
    }
    
}
